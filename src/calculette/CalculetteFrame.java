    private void buttonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEqualActionPerformed
        try
        {
            Double content = Double.parseDouble(calculationField.getText());
            
            switch (operator)
            {
                case OP_DIVISION:
                    result /= content;
                    break;
                
                case OP_MODULO:
                    result %= content;
                    break;
                    
                case OP_MULTIPLICATION:
                    result *= content;
                    break;
                    
                case OP_SUBSTRACTION:
                    result -= content;
                    break;
                    
                default:
                case OP_ADDITION:
                    result += content;
            }
            
            calculationField.setText(result.toString());
            result = 0d;
        }
        catch (NullPointerException e)
        {
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Ce nombre n'est pas d'un format valide.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonEqualActionPerformed

