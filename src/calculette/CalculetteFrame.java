    /**
     * Changement d'opérateur.
     */
    
    private void changeOperator(String operator)
    {
        try
        {
            Double content = Double.parseDouble(calculationField.getText());
            result = content;
            calculationField.setText("");
            
            this.operator = operator;
        }
        catch (NullPointerException e)
        {
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Ce nombre n'est pas d'un format valide.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    /**
     * Traitement du contenu du champ de texte.
     */
    
    private void addToCalculationField(String x)
    {
        String text = calculationField.getText();
        
        if (x.equals("."))
        {
            if (! text.matches("^.*\\..*$"))
            {
                calculationField.setText(text + ".");
            }
        }
        else if ( x.matches("^[0-9]$"))
        {
            calculationField.setText(text + x);
        }
    }
    
    
